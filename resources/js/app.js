import './bootstrap';
import './fontawesome'
import '../css/app.css';
import 'bootstrap/dist/css/bootstrap.css'
import 'admin-lte/dist/css/adminlte.css'
import '@fontsource/poppins/400.css';
import '@fontsource/poppins/500.css';
